export type I18nKey = keyof (typeof translations)["en"];

function isLanguageCode(
  value: string | null | undefined,
): value is UiLanguageCode {
  return Boolean(value && languageCodes.includes(value as UiLanguageCode));
}

function detectLanguage(): UiLanguageCode {
  if (typeof window === "undefined") {
    return defaultLanguage;
  }

  const storedLanguage = window.localStorage.getItem(storageKey);
  if (isLanguageCode(storedLanguage)) {
    return storedLanguage;
  }

  const browserLanguages = navigator.languages?.length
    ? navigator.languages
    : [navigator.language];
  const browserLanguage = browserLanguages
    .map((language) => language.toLowerCase().split("-")[0])
    .find(
      (language): language is UiLanguageCode =>
        language === "ru" || language === "tg",
    );

  return browserLanguage ?? defaultLanguage;
}

function applyDocumentLanguage(language: UiLanguageCode) {
  if (typeof document === "undefined") {
    return;
  }

  const languageMeta = uiLanguages.find((item) => item.code === language);
  document.documentElement.lang = language;
  document.documentElement.dir = languageMeta?.dir ?? "ltr";
}

export function useI18n() {
  const [language, setLanguageState] = useState<UiLanguageCode>(() =>
    detectLanguage(),
  );

  useEffect(() => {
    applyDocumentLanguage(language);

    function onLanguageChange(event: Event) {
      const nextLanguage = (event as CustomEvent<UiLanguageCode>).detail;
      if (isLanguageCode(nextLanguage)) {
        setLanguageState(nextLanguage);
      }
    }

    function onStorage(event: StorageEvent) {
      if (event.key === storageKey && isLanguageCode(event.newValue)) {
        setLanguageState(event.newValue);
      }
    }

    window.addEventListener(languageEventName, onLanguageChange);
    window.addEventListener("storage", onStorage);

    return () => {
      window.removeEventListener(languageEventName, onLanguageChange);
      window.removeEventListener("storage", onStorage);
    };
  }, [language]);

  const setLanguage = useCallback((nextLanguage: UiLanguageCode) => {
    window.localStorage.setItem(storageKey, nextLanguage);
    applyDocumentLanguage(nextLanguage);
    setLanguageState(nextLanguage);
    window.dispatchEvent(
      new CustomEvent(languageEventName, { detail: nextLanguage }),
    );
  }, []);

  const t = useCallback(
    (key: I18nKey) =>
      translations[language]?.[key] ?? translations.en[key] ?? key,
    [language],
  );

  return useMemo(
    () => ({
      language,
      setLanguage,
      t,
      languages: uiLanguages,
    }),
    [language, setLanguage, t],
  );
}
